  static List<Flight> get flights => List.unmodifiable(_flights);

  static List<Flight> searchFlights({
    required String origin,
    required String destination,
    String? date,
    double? maxPrice,
    String? airline,
  }) {
    var results = _flights.where((flight) {
      bool originMatch = flight.origin.toLowerCase().contains(origin.toLowerCase()) ||
          flight.originCode.toLowerCase().contains(origin.toLowerCase());
      bool destinationMatch = flight.destination.toLowerCase().contains(destination.toLowerCase()) ||
          flight.destinationCode.toLowerCase().contains(destination.toLowerCase());
      
      if (!originMatch || !destinationMatch) return false;
      
      if (maxPrice != null && flight.price > maxPrice) return false;
      
      if (airline != null && !flight.airline.toLowerCase().contains(airline.toLowerCase())) return false;
      
      return true;
    }).toList();

    results.sort((a, b) => a.price.compareTo(b.price));
    return results;
  }

  static Flight? getFlightById(String id) {
    try {
      return _flights.firstWhere((flight) => flight.id == id);
    } catch (e) {
      return null;
    }
  }

  static bool bookFlight(String id) {
    final index = _flights.indexWhere((flight) => flight.id == id);
    if (index != -1 && _flights[index].seats > 0) {
      _flights[index] = _flights[index].copyWith(seats: _flights[index].seats - 1);
      return true;
    }
    return false;
  }

